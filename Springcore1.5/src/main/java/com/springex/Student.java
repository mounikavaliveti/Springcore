package com.springex;
 
public class Student {
    private int sNo;
    private String sName;
     
    public int getStudentNo() {
        return sNo;
    }
    public void setStudentNo(int sNo) {
        this.sNo = sNo;
    }
    public String getStudentName() {
        return sName;
    }
    public void setStudentName(String sName) {
        this.sName = sName;
    }   
}